public class program6_operators {
    public static void main(String[] args) {

        // Arithmetic Operators +, -, *, /
        float a = 10;
        float b = 20;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // Relational Operators
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a != b);

        // Logical Operators AND, OR
        boolean x = true;
        boolean y = false;

        System.out.println(x && y);
        System.out.println(y && x);
        System.out.println(x || y);
        System.out.println(y || x);
    }
}
